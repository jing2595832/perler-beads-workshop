// 分类列表（自动从patterns数据生成）
export const categories = (() => {
  const catMap = new Map<string, { id: string; name: string; count: number }>();
  const catNames: Record<string, string> = {
    emoji: '表情符号', animal: '动物', food: '美食', text: '文字',
    geo: '几何', chinese: '国风', plant: '植物', holiday: '节日',
    daily: '日常', vehicle: '交通工具', advanced: '进阶'
  };
  for (const p of patterns) {
    if (!catMap.has(p.category)) {
      catMap.set(p.category, { id: p.category, name: catNames[p.category] || p.category, count: 0 });
    }
    catMap.get(p.category)!.count++;
  }
  return Array.from(catMap.values());
})();
